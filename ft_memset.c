#include "libft.h"

void	*ft_memset(void *ptr, int c, size_t num)
{
	size_t	i;
	char	*str;

	str = ptr;
	i = 0;
	while (i < num)
	{
		str[i] = c;
		i++;
	}
	return (ptr);
}
