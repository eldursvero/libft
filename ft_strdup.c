#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		length;
	int		i;
	char	*copy;

	i = 0;
	length = ft_strlen((char *)src) + 1;
	if (!(copy = (char *)malloc(sizeof(*src) * length)))
		return (0);
	while (src[i] != '\0')
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
